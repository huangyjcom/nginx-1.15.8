struct ngx_conf_s {
    char                 *name;
    ngx_array_t          *args;//解析一行得到的东西

    ngx_cycle_t          *cycle;
    ngx_pool_t           *pool;
    ngx_pool_t           *temp_pool;
    ngx_conf_file_t      *conf_file;//文件
    ngx_log_t            *log;

    void                 *ctx;//当前的主ctx信息
    ngx_uint_t            module_type;//模块的类型
    ngx_uint_t            cmd_type;//当前是哪类解析

    ngx_conf_handler_pt   handler;
    void                 *handler_conf;
};


