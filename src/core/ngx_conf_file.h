/*
 *        AAAA  number of arguments
 *      FF      command flags
 *    TT        command type, i.e. HTTP "location" or "server" command
 */

#define NGX_CONF_NOARGS      0x00000001
#define NGX_CONF_TAKE1       0x00000002
#define NGX_CONF_TAKE2       0x00000004
#define NGX_CONF_TAKE3       0x00000008
#define NGX_CONF_TAKE4       0x00000010
#define NGX_CONF_TAKE5       0x00000020
#define NGX_CONF_TAKE6       0x00000040
#define NGX_CONF_TAKE7       0x00000080

#define NGX_CONF_MAX_ARGS    8

#define NGX_CONF_TAKE12      (NGX_CONF_TAKE1|NGX_CONF_TAKE2) //配置指令接受1个或者2个参数
#define NGX_CONF_TAKE13      (NGX_CONF_TAKE1|NGX_CONF_TAKE3)

#define NGX_CONF_TAKE23      (NGX_CONF_TAKE2|NGX_CONF_TAKE3)

#define NGX_CONF_TAKE123     (NGX_CONF_TAKE1|NGX_CONF_TAKE2|NGX_CONF_TAKE3)
#define NGX_CONF_TAKE1234    (NGX_CONF_TAKE1|NGX_CONF_TAKE2|NGX_CONF_TAKE3   \
                              |NGX_CONF_TAKE4)

#define NGX_CONF_ARGS_NUMBER 0x000000ff
#define NGX_CONF_BLOCK       0x00000100 //配置指令可以接受的值是一个配置信息块。也就是一对大括号括起来的内容
#define NGX_CONF_FLAG        0x00000200 //配置指令可以接受的值是”on”或者”off”，最终会被转成bool值
#define NGX_CONF_ANY         0x00000400 //配置指令可以接受的任意的参数值。一个或者多个，或者”on”或者”off”，或者是配置块
#define NGX_CONF_1MORE       0x00000800 //配置指令接受至少一个参数
#define NGX_CONF_2MORE       0x00001000 //配置指令接受至少两个参数

#define NGX_DIRECT_CONF      0x00010000 //可以出现在配置文件中最外层。例如已经提供的配置指令daemon，master_process等。

#define NGX_MAIN_CONF        0x01000000 //http、mail、events、error_log等
#define NGX_ANY_CONF         0x1F000000 //该配置指令可以出现在任意配置级别上



#define NGX_CONF_UNSET       -1
#define NGX_CONF_UNSET_UINT  (ngx_uint_t) -1
#define NGX_CONF_UNSET_PTR   (void *) -1
#define NGX_CONF_UNSET_SIZE  (size_t) -1
#define NGX_CONF_UNSET_MSEC  (ngx_msec_t) -1


#define NGX_CONF_OK          NULL
#define NGX_CONF_ERROR       (void *) -1

#define NGX_CONF_BLOCK_START 1
#define NGX_CONF_BLOCK_DONE  2
#define NGX_CONF_FILE_DONE   3

#define NGX_CORE_MODULE      0x45524F43  /* "CORE" */
#define NGX_CONF_MODULE      0x464E4F43  /* "CONF" */


#define NGX_MAX_CONF_ERRSTR  1024


struct ngx_command_s {
    ngx_str_t             name;//配置项的名称
    ngx_uint_t            type;//配置项类型和接受参数的个数
    /*
	type取值:    
	1.NGX_DIRECT_CONF 单纯用来指定配置存储区的寻址方法，只用于core模块
	2.NGX_MAIN_CONF 有两重含义，其一是指定指令的使用上下文是main（其实还是指core模块），其二是指定配置存储区的寻址方法
	3.NGX_ANY_CONF 哪里都行
	4.NGX_CONF_BLOCK 代表着是一个块 就是{}包着
	5.一些模块内的比如NGX_EVENT_CONF 都是在块解析的时候改变模块信息
	6.接受参数的个数 NGX_CONF_NOARGS代表后面没有参数，
	  NGX_CONF_MAX_ARGS 参数的最大个数也就是八个 
	  NGX_CONF_TAKE1 .... NGX_CONF_TAKE7分布代表一到七个参数。
	  还支持组合（NGX_CONF_TAKE1|NGX_CONF_TAKE7）表示接受一个或七个参数
	*/
    char               *(*set)(ngx_conf_t *cf, ngx_command_t *cmd, void *conf);//处理配置项的回调方法。Nginx提供了14个预设的解析配置项的方法
    //Nginx首先通过conf成员找到应该用哪个结构体来存放，然后通过offset成员找到这个结构体中的相应成员，以便存放该配置
    ngx_uint_t            conf;//指定当前配置项存储的内存位置 和后面读取对应
    ngx_uint_t            offset;//存放信息的offset
    void                 *post;//一个指针 可以指向任何一个在读取配置过程中需要的数据，以便于进行配置读取的处理
};

#define ngx_null_command  { ngx_null_string, 0, NULL, 0, 0, NULL }


